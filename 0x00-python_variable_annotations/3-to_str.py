#!/usr/bin/env python3
"""
Module: conversion
This module provides functions for converting data types.

Functions:
- to_str(n: float) -> str: Converts a float to a string and returns the result.
"""


def to_str(n: float) -> str:
    """
    Convert a float to a string.

    Args:
    - n (float): The float number to be converted.

    Returns:
    - str: The float number converted to a string.
    """
    return str(n)
