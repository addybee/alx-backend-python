#!/usr/bin/env python3
"""
This module contains a function to Concatenates two strings.

Functions:
- concat: Concatenates two strings and returns the result.
"""


def concat(str1: str, str2: str) -> str:
    """
    Concatenates two strings and returns the result.

    Args:
        str1 (str): The first string to be concatenated.
        str2 (str): The second string to be concatenated.

    Returns:
        str: The concatenated string of str1 and str2.
    """
    return str1 + str2
