#!/usr/bin/env python3
"""
Module: math_operations

This module contains a function for flooring a floating-point number.

Functions:
- floor(n: float) -> int: Returns the floor value of the input floating-point
number.
"""


def floor(n: float) -> int:
    """
    Return the floor value of the input floating-point number.

    Args:
    - n (float): The input floating-point number.

    Returns:
    - int: The floor value of the input number.
    """
    return int(n)
